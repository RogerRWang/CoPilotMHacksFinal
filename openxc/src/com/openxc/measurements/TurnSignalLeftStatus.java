package com.openxc.measurements;

import com.openxc.units.Boolean;

/**
 * The BrakePedalStatus measurement knows if the brake pedal is pressed.
 */
public class TurnSignalLeftStatus extends BaseMeasurement<Boolean> {
    public final static String ID = "TurnInd_LT_ON";

    public TurnSignalLeftStatus(Boolean value) {
        super(value);
    }

    public TurnSignalLeftStatus(java.lang.Boolean value) {
        this(new Boolean(value));
    }

    @Override
    public String getGenericName() {
        return ID;
    }
}